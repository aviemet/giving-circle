const REGION_FLAG_OVERRIDES = {
	"419": "MX",
	"YUE": "HK",
	"CY": "CY",
	"AW": "AW",
	"CW": "CW",
}

function listJsonLocaleIds() {
	return fs.readdirSync(localesDir)
		.filter(name => name.endsWith(".json"))
		.map(name => name.replace(/\.json$/, ""))
		.sort((left, right) => left.localeCompare(right))
}

function listDayjsLocaleIds() {
	const ids = new Set()
	for(const name of fs.readdirSync(dayjsLocaleDir)) {
		if(!name.endsWith(".js")) continue
		if(name.endsWith(".d.ts")) continue
		ids.add(name.replace(/\.js$/, ""))
	}
	return ids
}

function languageSubtag(appLocaleId) {
	return appLocaleId.replace(/_/g, "-").split("-")[0].toLowerCase()
}

function regionSubtag(appLocaleId) {
	const normalized = appLocaleId.replace(/_/g, "-")
	const parts = normalized.split("-")
	if(parts.length < 2) return undefined
	return parts[parts.length - 1]
}

function resolveDayjsLocale(appLocaleId, dayjsIds) {
	const hyphenated = appLocaleId.replace(/_/g, "-")
	const exact = hyphenated.toLowerCase()
	if(dayjsIds.has(exact)) return exact

	const language = languageSubtag(appLocaleId)
	if(dayjsIds.has(language)) return language

	return "en"
}

const INTL_LOCALE_OVERRIDES = {
	en: "en-US",
	ua: "uk",
	"zh-YUE": "yue",
}

function resolveIntlLocale(appLocaleId) {
	const override = INTL_LOCALE_OVERRIDES[appLocaleId]
	if(override !== undefined) return override
	return appLocaleId.replace(/_/g, "-")
}

function resolveFlagRegion(appLocaleId) {
	const region = regionSubtag(appLocaleId)
	if(region !== undefined) {
		const upper = region.toUpperCase()
		if(REGION_FLAG_OVERRIDES[upper] !== undefined && hasFlag(REGION_FLAG_OVERRIDES[upper])) {
			return REGION_FLAG_OVERRIDES[upper]
		}
		if(/^[A-Z]{2}$/.test(upper) && hasFlag(upper)) return upper
		if(REGION_FLAG_OVERRIDES[region] !== undefined && hasFlag(REGION_FLAG_OVERRIDES[region])) {
			return REGION_FLAG_OVERRIDES[region]
		}
	}

	const language = languageSubtag(appLocaleId)
	const mapped = LANGUAGE_FLAG_REGIONS[language]
	if(mapped !== undefined && hasFlag(mapped)) return mapped

	return "US"
}

function generate() {
	const appLocaleIds = listJsonLocaleIds()
	const dayjsIds = listDayjsLocaleIds()

	if(!dayjsIds.has("en")) {
		throw new Error("dayjs locale 'en' is missing")
	}

	const entries = appLocaleIds.map(id => {
		const dayjsLocale = resolveDayjsLocale(id, dayjsIds)
		if(!dayjsIds.has(dayjsLocale)) {
			throw new Error(`Resolved dayjs locale '${dayjsLocale}' for '${id}' does not exist`)
		}

		return {
			id,
			intlLocale: resolveIntlLocale(id),
			dayjsLocale,
			flagRegion: resolveFlagRegion(id),
		}
	})

	for(const entry of entries) {
		if(!hasFlag(entry.flagRegion)) {
			throw new Error(`flagRegion '${entry.flagRegion}' for locale '${entry.id}' is not supported by country-flag-icons`)
		}
	}

	const lines = [
		"/* eslint-disable */",
		"/* This file is generated by scripts/generate-locale-registry.mjs — do not edit by hand. */",
		"import type { LocaleDefinition } from \"./types\"",
		"",
		"export const localeRegistry: Record<string, LocaleDefinition> = {",
	]

	for(const entry of entries) {
		lines.push(
			`\t${JSON.stringify(entry.id)}: { id: ${JSON.stringify(entry.id)}, intlLocale: ${JSON.stringify(entry.intlLocale)}, dayjsLocale: ${JSON.stringify(entry.dayjsLocale)}, flagRegion: ${JSON.stringify(entry.flagRegion)} },`,
		)
	}

	lines.push("}", "")
	lines.push(`export const registryLocaleIds = ${JSON.stringify(entries.map(entry => entry.id), null, "\t")} as const`, "")
	lines.push("export type RegistryLocaleId = typeof registryLocaleIds[number]", "")

	fs.mkdirSync(path.dirname(outputPath), { recursive: true })
	fs.writeFileSync(outputPath, `${lines.join("\n")}\n`)

	console.log(`Wrote ${entries.length} locales to ${path.relative(root, outputPath)}`)
}

generate()
